package com.vo2dto;

public class UserVO {
    private String name;

    private String id;

    private String nameVO;

    public String getNameVO() {
        return nameVO;
    }

    public void setNameVO(String nameVO) {
        this.nameVO = nameVO;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
